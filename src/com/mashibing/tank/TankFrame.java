package com.mashibing.tank;

import java.awt.Color;
import java.awt.Frame;
import java.awt.Graphics;
import java.awt.Image;
import java.awt.event.KeyAdapter;
import java.awt.event.KeyEvent;
import java.awt.event.WindowAdapter;
import java.awt.event.WindowEvent;
import java.util.ArrayList;
import java.util.Iterator;
import java.util.List;

//继承Frame
public class TankFrame extends Frame{
	GameModel gm = GameModel.getInstance();
	
	static final int GAME_WIDTH = 1080, GAME_HEIGHT = 960;
	
	
	
	public TankFrame() {
		setSize(GAME_WIDTH, GAME_HEIGHT);
		setResizable(false);  //是否可以改变大小
		setTitle("tank war");
		setVisible(true);
		
		this.addKeyListener(new MyKeyListener());
		
		//添加监听器，现在监听着closing这件事
		addWindowListener(new WindowAdapter() {

			@Override
			public void windowClosing(WindowEvent e) {
				System.exit(0);
			}
			
		});
	}
	
	/**
	 * 双缓存解决闪烁
	 * 闪烁是因为在显示大图时，部分图像还没计算完，
	 * 有部分图像算完显示在屏幕上了
	 * 解决：在内存定义同样大小的空间，先画在内存，画完再一次性画在屏幕上
	 */
	Image offScreenImage = null;   //定义一张图片，在内存
	@Override
	public void update(Graphics g) {  //g是屏幕的画笔
		if(offScreenImage == null)
			offScreenImage = this.createImage(GAME_WIDTH,GAME_HEIGHT);
		Graphics gOffScreen = offScreenImage.getGraphics();   //获取画笔
		Color c = gOffScreen.getColor();
		gOffScreen.setColor(Color.BLACK);
		gOffScreen.fillRect(0, 0, GAME_WIDTH, GAME_HEIGHT);   //重画背景
		gOffScreen.setColor(c);
		paint(gOffScreen);  //传递当前画笔，画在内存的
		g.drawImage(offScreenImage,0,0,null);   //图片一次性画在屏幕
	}
	
	//每次绘制会清空后再画，最小化再显示也会再绘制
	@Override
	public void paint(Graphics g) {	
		gm.paint(g);
	
	}
	
	//定义一个监听键盘的类，使得通过按键使矩形移动
	//一个观察者，因为这个观察者类在事件源类中
	//所以不需要传事件源对象，如果定义在类之外
	//需要传入事件源对象，在刷新等操作中需要事件源
	class MyKeyListener extends KeyAdapter{

		//用boolean表示按下的键,这样方向可以组合
		//有可能同时按下上下键，但在计算机时有一个先后顺序的，即使看上去同时
		boolean bL = false;
		boolean bU = false;
		boolean bR = false;
		boolean bD = false;
		
		//键被按下时调用
		@Override
		public void keyPressed(KeyEvent e) {
			int key = e.getKeyCode();
			//这里不能斜着移动，因为switch一次改一次
			switch (key) {
			case KeyEvent.VK_LEFT:
				bL = true;
				break;
			case KeyEvent.VK_UP:
				bU = true;
				break;
			case KeyEvent.VK_RIGHT:
				bR = true;
				break;
			case KeyEvent.VK_DOWN:
				bD = true;
				break;
			case KeyEvent.VK_S:
				gm.save();
				break;
			case KeyEvent.VK_L:
				gm.load();
				break;	

			}
			
			setMainTankDir();
			new Thread(()->new Audio("audio/tank_move.wav").play()).start();
		}

		//键松开时调用
		@Override
		public void keyReleased(KeyEvent e) {
			//键抬起时，恢复
			int key = e.getKeyCode();
			switch (key) {
			case KeyEvent.VK_LEFT:
				bL = false;
				break;
			case KeyEvent.VK_UP:
				bU = false;
				break;
			case KeyEvent.VK_RIGHT:
				bR = false;
				break;
			case KeyEvent.VK_DOWN:
				bD = false;
				break;
			case KeyEvent.VK_CONTROL:
				gm.getMainTank().fire();
				//gm.getMainTank().handleFireKey();
				break;
			default:break;
				
			}
			
			setMainTankDir();
		}

		private void setMainTankDir() {
			if(!bL && !bU &&!bR && !bD)
				gm.getMainTank().setMoving(false);
			else
				gm.getMainTank().setMoving(true);
			if(bL) gm.getMainTank().setDir(Dir.LEFT);
			if(bU) gm.getMainTank().setDir(Dir.UP);
			if(bR) gm.getMainTank().setDir(Dir.RIGHT);
			if(bD) gm.getMainTank().setDir(Dir.DOWN);
		}
		
	}
}
